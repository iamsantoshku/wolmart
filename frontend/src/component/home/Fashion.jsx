import React, { useEffect, useState } from "react";
import axios from "axios";
import { BACKENDURL } from "../../config/config";

const Fashion = () => {
  const [products, setProducts] = useState([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);

  useEffect(() => {
    const fetchFashionProducts = async () => {
      try {
        const response = await axios.get(`${BACKENDURL}/api/v1/product/allproduct`);
        setProducts(response.data.data);
      } catch (err) {
        setError("Failed to fetch products");
      } finally {
        setLoading(false);
      }
    };

    fetchFashionProducts();
  }, []);

  return (
    <div className="col-lg-9 col-sm-8">
      <div
        className="swiper-container swiper-theme"
        data-swiper-options={`{
          "spaceBetween": 20,
          "slidesPerView": 2,
          "breakpoints": {
              "992": { "slidesPerView": 3 },
              "1200": { "slidesPerView": 4 }
          }
        }`}
      >
        <div className="swiper-wrapper row cols-xl-4 cols-lg-3 cols-2">
          {loading ? (
            <p>Loading products...</p>
          ) : error ? (
            <p className="text-danger">{error}</p>
          ) : products.length === 0 ? (
            <p>No products found in this category.</p>
          ) : (
            products.slice(-6).map((product) => (
              <div key={product._id} className="swiper-slide product-col">
                <div className="product-wrap product text-center">
                  <figure className="product-media">
                <a href={`/product/${product.name}`}>
                    {/* <Link to={} className="block"> */}
                      <img
                        src={`${BACKENDURL}/uploads/product/${product.images?.[0]?.urls?.[0].split("\\").pop()}`}
                        alt={product.name}
                        width="216"
                        height="243"
                        style={{ objectFit: "cover", width: "216px", height: "243px" }}
                      />
                    </a>
                    <div className="product-action-vertical">
                      <a href="#" className="btn-product-icon btn-cart w-icon-cart" title="Add to cart"></a>
                      <a href="#" className="btn-product-icon btn-wishlist w-icon-heart" title="Add to wishlist"></a>
                      <a href="#" className="btn-product-icon btn-quickview w-icon-search" title="Quickview"></a>
                      <a href="#" className="btn-product-icon btn-compare w-icon-compare" title="Add to Compare"></a>
                    </div>
                  </figure>
                  <div className="product-details">
                    <h4 className="product-name">
                      <a href="product-default.html">{product.name}</a>
                    </h4>
                    <div className="ratings-container">
                      <div className="ratings-full">
                        <span className="ratings" style={{ width: `${(product.rating || 0) * 20}%` }}></span>
                        <span className="tooltiptext tooltip-top"></span>
                      </div>
                      <a href="product-default.html" className="rating-reviews">
                        ({product.reviews?.length || 0} reviews)
                      </a>
                    </div>
                    <div className="product-price">
                      <ins className="new-price">${product.price.toFixed(2)}</ins>
                      {product.oldPrice && <del className="old-price">${product.oldPrice.toFixed(2)}</del>}
                    </div>
                  </div>
                </div>
              </div>
            ))
          )}
        </div>
        <div className="swiper-pagination"></div>
      </div>
    </div>
  );
};

export default Fashion;
